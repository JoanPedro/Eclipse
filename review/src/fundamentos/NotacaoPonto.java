package fundamentos;

public class NotacaoPonto {
	public static void main(String[] args) {

		String s = "Bom dia x";
		var upperCase = s.toUpperCase();
		upperCase = s.replace("x", "Joan Pedro");
		upperCase = upperCase.concat("!!!");
		System.out.println(upperCase);
		
		String y = "Joan x Oliveira de Souza"
				.replace("x", "Pedro")
				.toUpperCase()
				.concat("!!!");
		System.out.println(y);
	}
}
